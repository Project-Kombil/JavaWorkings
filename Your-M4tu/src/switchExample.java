public class switchExample {
    public static void main(String[] args){
        int value = 5;
        switch (value){
            case 1 : System.out.println("The value is false");
            break;
            case 2 : System.out.println("The value is false");
            break;
            case 5 : System.out.println("The value is true");
            break;
            default : System.out.println("Neither is correct");
        }
    }
}
